    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rollNo=jTextField2.getText();
        String physics=jTextField1.getText();
        String math=jTextField3.getText();
        String dbms=jTextField4.getText();
        String os=jTextField5.getText();
        String oops=jTextField6.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","password123");
            Statement st=con.createStatement();
            ResultSet rs= st.executeQuery("select student.rollNo from student where student.rollNo='"+rollNo+"'");
            if(rs.next()){
                st.executeUpdate("insert into result values('"+rollNo+"','"+physics+"','"+math+"','"+dbms+"','"+os+"','"+oops+"')");
                JOptionPane.showMessageDialog(null,"result saved successfuly");
            }else JOptionPane.showMessageDialog(null,"Student is not registered!");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

